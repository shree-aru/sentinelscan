import time
from datetime import datetime
from typing import List, Optional

from fastapi import FastAPI, HTTPException, BackgroundTasks
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel, HttpUrl, Field

from scanner.core import SentinelCore, normalize_target, validate_target
from scanner.models import Severity


# ─────────────────────────────────────────────────────────────────────────────
#  PYDANTIC SCHEMAS
#  Pydantic is FastAPI's validation library. You define a class that inherits
#  from BaseModel, and FastAPI automatically validates request/response data.
#  Invalid requests get a clear 422 error — no manual validation needed.
# ─────────────────────────────────────────────────────────────────────────────

class ScanRequest(BaseModel):
    """Request body for POST /scan"""
    target: str = Field(
        ...,
        description="Target URL to scan (e.g., https://example.com)",
        example="https://example.com"
    )
    timeout: int = Field(
        default=10,
        ge=3,
        le=30,
        description="Request timeout per module in seconds (3-30)"
    )
    include_threat_intel: bool = Field(
        default=True,
        description="Include Shodan + NVD threat intelligence lookup"
    )
    modules: Optional[List[str]] = Field(
        default=None,
        description="Specific modules to run. Null = all modules.",
        example=["headers", "ssl", "cors"]
    )


class FindingResponse(BaseModel):
    """A single vulnerability finding in the response."""
    title: str
    severity: str
    cvss_score: float
    description: str
    recommendation: str
    evidence: str


class ModuleResponse(BaseModel):
    """Results from one scanner module."""
    module: str
    findings: List[FindingResponse]
    passed_checks: List[str]
    error: str


class ScanSummary(BaseModel):
    """Summary counts of findings by severity."""
    overall_risk: str
    total_findings: int
    critical: int
    high: int
    medium: int
    low: int
    modules_run: List[str]
    scan_duration_seconds: float


class ScanResponse(BaseModel):
    """Complete API response for a scan."""
    sentinelscan_version: str = "1.0"
    target: str
    scan_time: str
    summary: ScanSummary
    modules: List[ModuleResponse]


