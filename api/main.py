#  HELPER FUNCTION
# ─────────────────────────────────────────────────────────────────────────────

def report_to_response(report, duration: float) -> ScanResponse:
    """
    Convert a ScanReport object (our internal model) to a ScanResponse
    (Pydantic model for API output). This is the "serialization" step.
    """
    modules_out = []
    for result in report.results:
        findings_out = [
            FindingResponse(
                title=f.title,
                severity=f.severity.value,
                cvss_score=f.cvss_score,
                description=f.description,
                recommendation=f.recommendation,
                evidence=f.evidence
            )
            for f in result.findings
        ]
        modules_out.append(ModuleResponse(
            module=result.module_name,
            findings=findings_out,
            passed_checks=result.passed,
            error=result.error
        ))

    return ScanResponse(
        target=report.target,
        scan_time=report.scan_time,
        summary=ScanSummary(
            overall_risk=report.risk_rating(),
            total_findings=report.total_findings(),
            critical=report.critical_count(),
            high=report.high_count(),
            medium=report.medium_count(),
            low=report.low_count(),
            modules_run=report.modules_run,
            scan_duration_seconds=round(duration, 2)
        ),
        modules=modules_out
    )


