@app.post("/scan", response_model=ScanResponse, tags=["Scanner"])
async def run_full_scan(request: ScanRequest):
    """
    Run a complete security scan against the target URL.

    Executes all scanner modules:
    - Security Headers
    - SSL/TLS Certificate
    - Sensitive Files and Directories
    - SQL Injection
    - Reflected XSS
    - CORS Misconfiguration
    - Threat Intelligence (Shodan + NVD CVEs) — if enabled

    Returns a full JSON report with findings, severity ratings, and fix recommendations.

    **Note:** Full scans take 30-120 seconds depending on the target's response time
    and the number of CVEs looked up from NVD.
    """
    # Validate and normalize the target URL
    target = normalize_target(request.target)
    is_valid, error_msg = validate_target(target)

    if not is_valid:
        raise HTTPException(
            status_code=422,
            detail=f"Invalid target URL: {error_msg}"
        )

    start_time = time.time()

    try:
        scanner = SentinelCore(
            timeout=request.timeout,
            include_threat_intel=request.include_threat_intel
        )
        report = scanner.scan(target)
    except Exception as e:
        raise HTTPException(
            status_code=500,
            detail=f"Scan failed with internal error: {str(e)[:200]}"
        )

    duration = time.time() - start_time
    return report_to_response(report, duration)


@app.post("/scan/quick", response_model=ScanResponse, tags=["Scanner"])
async def run_quick_scan(request: ScanRequest):
    """
    Run a quick scan using only fast, non-injecting modules.

    Modules included:
    - Security Headers (fast — single HTTP request)
    - SSL/TLS Certificate (fast — single TLS handshake)
    - CORS Misconfiguration (fast — 2-3 HTTP requests)

    Modules excluded:
    - SQL Injection (slow — multiple payloads per parameter)
    - XSS (slow — multiple payloads per parameter)
    - Sensitive Files (moderate — 47 HTTP requests)
    - Threat Intelligence (slow — external API calls)

    Typical duration: 3-8 seconds.
    Suitable for: CI/CD pipeline checks, frequent monitoring.
    """
    target = normalize_target(request.target)
    is_valid, error_msg = validate_target(target)

    if not is_valid:
        raise HTTPException(status_code=422, detail=f"Invalid target URL: {error_msg}")

    start_time = time.time()

    try:
        scanner = SentinelCore(
            timeout=request.timeout,
            modules=["headers", "ssl", "cors"],
            include_threat_intel=False
        )
        report = scanner.scan(target)
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Scan failed: {str(e)[:200]}")

    duration = time.time() - start_time
    return report_to_response(report, duration)
